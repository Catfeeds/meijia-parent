package com.simi.vo.user;

public class UserImLastVo {
	
	private String headImg;
	
	private String name;
	
	private String toImUserName;
	
	private String imContent;
	
	private Long addTime;

	public String getHeadImg() {
		return headImg;
	}

	public void setHeadImg(String headImg) {
		this.headImg = headImg;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getToImUserName() {
		return toImUserName;
	}

	public void setToImUserName(String toImUserName) {
		this.toImUserName = toImUserName;
	}

	public String getImContent() {
		return imContent;
	}

	public void setImContent(String imContent) {
		this.imContent = imContent;
	}

	public Long getAddTime() {
		return addTime;
	}

	public void setAddTime(Long addTime) {
		this.addTime = addTime;
	}

}
